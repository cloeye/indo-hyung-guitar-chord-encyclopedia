import type { ChordRoot, ChordVoicing } from "@/types/chord";

export type OpenChordCollectionId = "G" | "D" | "E" | "A" | "C";

export type OpenChordCollection = {
  id: OpenChordCollectionId;
  title: string;
  subtitle: string;
  progression: string;
  voicings: ChordVoicing[];
  advancedVoicings: ChordVoicing[];
};

type OpenChordSeed = {
  id: string;
  chordName: string;
  displayName: string;
  root: ChordRoot;
  qualityId: string;
  startFret?: number;
  frets: ChordVoicing["frets"];
  fingers?: ChordVoicing["fingers"];
  rootStrings: number[];
  barre?: ChordVoicing["barre"];
  voicingType?: ChordVoicing["voicingType"];
  difficulty?: ChordVoicing["difficulty"];
  includedDegrees: string[];
  omittedDegrees?: string[];
  collisionRisk?: ChordVoicing["collisionRisk"];
  isRecommendedForBeginner?: boolean;
  description: string;
  caution?: string;
};

function getMaxFret(frets: ChordVoicing["frets"]) {
  const frettedValues = frets.filter((fret): fret is number => typeof fret === "number");

  return Math.max(...frettedValues);
}

function hasAnyDegree(degrees: string[], candidates: string[]) {
  return degrees.some((degree) => candidates.includes(degree));
}

function assignOpenChordFingers(frets: ChordVoicing["frets"]) {
  const uniqueFrets = [
    ...new Set(frets.filter((fret): fret is number => typeof fret === "number" && fret > 0)),
  ].sort((a, b) => a - b);
  const fingerByFret = new Map(uniqueFrets.map((fret, index) => [fret, Math.min(index + 1, 4)]));

  return frets.map((fret) => {
    if (fret === "x") return "x";
    if (fret === 0) return 0;
    return fingerByFret.get(fret) ?? 4;
  }) as ChordVoicing["fingers"];
}

function openChord(seed: OpenChordSeed): ChordVoicing {
  const maxFret = getMaxFret(seed.frets);
  const includedDegrees = seed.includedDegrees;

  return {
    id: seed.id,
    chordName: seed.chordName,
    displayName: seed.displayName,
    root: seed.root,
    qualityId: seed.qualityId,
    startFret: seed.startFret ?? 1,
    maxFret,
    frets: seed.frets,
    fingers: seed.fingers ?? assignOpenChordFingers(seed.frets),
    rootStrings: seed.rootStrings,
    barre: seed.barre,
    voicingType: seed.voicingType ?? "open",
    difficulty: seed.difficulty ?? "easy",
    useCase: ["beginner", "band", "worship"],
    includedDegrees,
    omittedDegrees: seed.omittedDegrees ?? [],
    hasRoot: includedDegrees.includes("1"),
    hasThird: hasAnyDegree(includedDegrees, ["3", "b3"]),
    hasFifth: includedDegrees.includes("5"),
    hasSeventh: hasAnyDegree(includedDegrees, ["7", "b7"]),
    hasTension: hasAnyDegree(includedDegrees, ["2", "4", "9", "11", "13", "b13", "#11", "b9"]),
    collisionRisk: seed.collisionRisk ?? "low",
    isLowPosition: maxFret <= 5,
    isRecommendedForBeginner:
      seed.isRecommendedForBeginner ?? ((seed.difficulty ?? "easy") === "easy" && maxFret <= 5),
    isFree: true,
    description: seed.description,
    caution: seed.caution,
  };
}

function advancedOpenChord(seed: OpenChordSeed): ChordVoicing {
  return openChord({
    ...seed,
    difficulty: seed.difficulty ?? "hard",
    voicingType: seed.voicingType ?? "advanced",
    isRecommendedForBeginner: seed.isRecommendedForBeginner ?? false,
  });
}

type TabFret = number | "x";
type TabFrets = [TabFret, TabFret, TabFret, TabFret, TabFret, TabFret];
type ReferenceTensionSeed = Omit<
  OpenChordSeed,
  "frets" | "fingers" | "rootStrings" | "description"
> & {
  tabFrets: TabFrets;
  rootStrings?: number[];
  description?: string;
};

function fretsFromTab(tabFrets: TabFrets) {
  return [
    tabFrets[5],
    tabFrets[4],
    tabFrets[3],
    tabFrets[2],
    tabFrets[1],
    tabFrets[0],
  ] as ChordVoicing["frets"];
}

function getReferenceStartFret(frets: ChordVoicing["frets"]) {
  const frettedValues = frets.filter((fret): fret is number => typeof fret === "number" && fret > 0);
  const minFret = Math.min(...frettedValues);

  return minFret <= 2 ? 1 : minFret;
}

function referenceTensionChord(seed: ReferenceTensionSeed) {
  const frets = fretsFromTab(seed.tabFrets);

  return advancedOpenChord({
    ...seed,
    frets,
    rootStrings: seed.rootStrings ?? [],
    startFret: seed.startFret ?? getReferenceStartFret(frets),
    description:
      seed.description ??
      "위쪽 개방현을 유지한 채 포지션을 이동해 모던워십 특유의 열린 텐션을 만드는 폼입니다.",
  });
}

