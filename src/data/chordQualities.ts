export const qualityTabLabels: Record<QualityTabId, string> = {
  main: "일반코드",
  altered: "얼터드코드",
  slash: "분수코드",
};

const alteredQualityIds = new Set([
  "major11",
  "major13",
  "minor11",
  "minorMajor9",
  "minorSixNine",
  "eleven",
  "dominant13",
  "dominant7flat9",
  "dominant7sharp9",
  "dominant9flat5",
  "dominant9sharp11",
  "aug9",
]);

export const qualityTabs: QualityTabId[] = ["main", "altered", "slash"];

export function getQualityTab(quality: ChordQuality): QualityTabId {
  if (quality.group === "slash") {
    return "slash";
  }

  if (alteredQualityIds.has(quality.id)) {
    return "altered";
  }

  return "main";
}

export function normalizeQualityTabId(value: string): QualityTabId {
  if (value === "altered" || value === "slash") {
    return value;
  }

  return "main";
}

export function getQualitiesForTab(tabId: QualityTabId) {
  return chordQualities.filter((quality) => getQualityTab(quality) === tabId);
}

